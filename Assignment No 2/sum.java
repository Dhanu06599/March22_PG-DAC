//Write a Java program to print the sum of two numbers.
import java.util.*;
class sum
{
	public static void main (String[] args)
	{
		System.out.print("Enter two digit ");
		Scanner sc =new Scanner (System.in);
		int x = sc.nextInt(); //74
		int y = sc.nextInt(); // 36
		int z = x+y; // 74+36 
		System.out.print(z); //100
		
	}
	
}